package nandu;

public class shift {
	public static void main(String[] args) {
		
		//right
		System.out.println(10>>5);
		System.out.println(10>>2);
		System.out.println(20>>4);
		
		//left
		System.out.println(10<<5);
		System.out.println(10<<2);
		System.out.println(20<<4);
	}

}
